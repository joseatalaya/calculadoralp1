public static String calculadora (String mem1, String mem2, String sig){
    Double resultado = 0.0;
    String respuesta;
    if(sig.equals("-")){
        resultado = Double.parseDouble(mem1)- Double.parseDouble(mem2);
    }
    if(sig.equals("+")){
        resultado = Double.parseDouble(mem1)+ Double.parseDouble(mem2);
    }
    if(sig.equals("*")){
        resultado = Double.parseDouble(mem1)* Double.parseDouble(mem2);
    }
    if(sig.equals("/")){
        resultado = Double.parseDouble(mem1)/ Double.parseDouble(mem2);
    }
    respuesta = resultado.toString();
    return respuesta;
}
   
